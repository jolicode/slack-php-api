    }

    public function normalize($object, $format = null, array $context = [])
    {
        $data = [];
        $data['count'] = $object->getCount();
        $data['name'] = $object->getName();
        $values = [];
        foreach ($object->getUsers() as $value) {
            $values[] = $value;
        }
        $data['users'] = $values;
        foreach ($object as $key => $value_1) {
            if (preg_match('/.*/', (string) $key)) {
                $data[$key] = $value_1;
            }
        }

        return $data;
    }
